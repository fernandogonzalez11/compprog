using namespace std;

void solve() {
    int n;
	cin >> n;

	int cnt = 0;
	int x = 0;
	while (2*x <= n) {
		int y = (n-2*x)/4;
		if (2*x + 4*y == n) cnt++;
		x++;
	}

	cout << cnt << "\n";
}

signed main() {
	ios::sync_with_stdio(false);
	cin.tie(0);
	
	int t = 1;
	cin >> t;
	while (t--) solve();

	return 0;
}
